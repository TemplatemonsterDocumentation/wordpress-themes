<h3>How to update the theme</h3>

<p>
    In order to update the theme, please check the available Framework/Plugins/Theme updates using the WordPress update
   bar or using the appropriate WordPress update section. Just click on any element you want to update and proceed. The
   update process is exactly the same as WordPress core update.
</p>

