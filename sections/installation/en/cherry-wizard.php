	<h3>Automatic installation using Cherry Wizard</h3>
	<ol>
		<li>Open your template delivery page. You will find the License key and link to download Monstroid Wizard plugin. Download the plugin by clicking on the link or opening it in a new window:
		<figure class="img-polaroid"><img src="img/monstroid/how-to-install-a-theme-1.jpg" alt=""></figure>
		</li>
		<li>Open WordPress dashboard, go to <strong>Plugins > Add New</strong>. Click on <strong>Upload Plugin</strong> button:
		<figure class="img-polaroid"><img src="img/monstroid/how-to-install-a-theme-2.jpg" alt=""></figure>
		</li>
		<li>Choose the plugin you have downloaded previously and install it:
		<figure class="img-polaroid"><img src="img/monstroid/how-to-install-a-theme-3.jpg" alt=""></figure>
		</li>
		<li>Activate Monstroid Wizard plugin:
		<figure class="img-polaroid"><img src="img/monstroid/how-to-install-a-theme-4.jpg" alt=""></figure>
		</li>
		<li>Go to <strong>Tools > Monstroid Wizard</strong>. You should enter the activation key in order to start theme installation process:
		<figure class="img-polaroid"><img src="img/monstroid/how-to-install-a-theme-5.jpg" alt=""></figure>
		</li>
		<li>Activation Key is specified on the template delivery page. Open your template download page and copy the License key:
		<figure class="img-polaroid"><img src="img/monstroid/how-to-install-a-theme-6.jpg" alt=""></figure>
		</li>
		<li>Paste the activation key. Click <strong>Start</strong> in order to perform installation:
		<figure class="img-polaroid"><img src="img/monstroid/how-to-install-a-theme-7.jpg" alt=""></figure>
		</li>
		<li>You will see Monstroid Main theme and Child themes. Select theme you would like to install. We are going to install Monstroid Main Theme:
		<figure class="img-polaroid"><img src="img/monstroid/how-to-install-a-theme-8.jpg" alt=""></figure>
		</li>
		<li>Installation process may take some time till all files are uploaded:
		<figure class="img-polaroid"><img src="img/monstroid/how-to-install-a-theme-9.jpg" alt=""></figure>
		</li>
		<li>Now you should select location of sample data. In most cases sample data should be uploaded from <strong>cloud</strong>:
		<figure class="img-polaroid"><img src="img/monstroid/how-to-install-a-theme-10.jpg" alt=""></figure>
		</li>
		<li>Sample data was downloaded from cloud. You can continue installation: 
		<figure class="img-polaroid"><img src="img/monstroid/how-to-install-a-theme-11.jpg" alt=""></figure>
		</li>
		<li>Sample data installation is in progress: 
		<figure class="img-polaroid"><img src="img/monstroid/how-to-install-a-theme-12.jpg" alt=""></figure>
		</li>
		<li>Theme and sample data were installed successfully: 
		<figure class="img-polaroid"><img src="img/monstroid/how-to-install-a-theme-13.jpg" alt=""></figure>
		</li>
		<li>Please check your site: 
		<figure class="img-polaroid"><img src="img/monstroid/how-to-install-a-theme-14.jpg" alt=""></figure>
		</li>
	</ol>
	<p>Feel free to check the detailed <a href="https://www.youtube.com/watch?v=SLsKPLEn5qg" target="_blank">video tutorial</a>.</p>
