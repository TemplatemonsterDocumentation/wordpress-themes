<h3>Automatic installation using Monstroid Wizard</h3>

<p>
    Open your template delivery page. You will find the License key and link to download Monstroid Wizard plugin.
    Download the plugin by clicking on the link or opening it in a new window:
</p>

<figure class="img-polaroid"><img src="img/monstroid/how-to-install-a-theme-1.jpg" alt=""></figure>

<p>
    Open WordPress dashboard, go to <strong>Plugins > Add New</strong>. Click on <strong>Upload Plugin</strong>
    button:
</p>

<figure class="img-polaroid"><img src="img/monstroid/how-to-install-a-theme-2.jpg" alt=""></figure>

<p>
    Choose the plugin you have downloaded previously and install it:
</p>

<figure class="img-polaroid"><img src="img/monstroid/how-to-install-a-theme-3.jpg" alt=""></figure>

<p>
    Activate Monstroid Wizard plugin:
</p>

<figure class="img-polaroid"><img src="img/monstroid/how-to-install-a-theme-4.jpg" alt=""></figure>

<p>
    Go to <strong>Tools > Monstroid Wizard</strong>. You should enter the activation key in order to start theme
    installation process:
</p>

<figure class="img-polaroid"><img src="img/monstroid/how-to-install-a-theme-5.jpg" alt=""></figure>

<p>
    Activation Key is specified on the template delivery page. Open your template download page and copy the License
    key:
</p>

<figure class="img-polaroid"><img src="img/monstroid/how-to-install-a-theme-1.jpg" alt=""></figure>

<p>
    Paste the activation key. Click <strong>Start</strong> in order to perform installation:
</p>

<figure class="img-polaroid"><img src="img/monstroid/how-to-install-a-theme-7.jpg" alt=""></figure>

<p>
    You will see Monstroid Main theme and Child themes. Select theme you would like to install. We are going to
    install Monstroid Main Theme (1):
</p>

<figure class="img-polaroid"><img src="img/monstroid/how-to-install-a-theme-8.jpg" alt=""></figure>

<p>
    Installation process may take some time till all files are uploaded:
</p>

<figure class="img-polaroid"><img src="img/monstroid/how-to-install-a-theme-9.jpg" alt=""></figure>

<p>
    Now you should select location of sample data. In most cases sample data should be uploaded from <strong>cloud</strong>:
</p>

<figure class="img-polaroid"><img src="img/monstroid/how-to-install-a-theme-10.jpg" alt=""></figure>

<p>
    Sample data was downloaded from cloud. You can continue installation:
</p>

<figure class="img-polaroid"><img src="img/monstroid/how-to-install-a-theme-11.jpg" alt=""></figure>

<p>
    Sample data installation is in progress:
</p>

<figure class="img-polaroid"><img src="img/monstroid/how-to-install-a-theme-12.jpg" alt=""></figure>

<p>
    Theme and sample data were installed successfully:
</p>

<figure class="img-polaroid"><img src="img/monstroid/how-to-install-a-theme-13.jpg" alt=""></figure>

<p>
    Please check your site:
</p>

<figure class="img-polaroid"><img src="img/monstroid/how-to-install-a-theme-14.jpg" alt=""></figure>

