	<h3>Page templates</h3>
	<p>Monstroid Offers 9 Template Page Attributes:</p>
	<ul>
		<li><strong>Default Template:</strong> this is the default page template that automatically has the sidebar with it. Choose this template for your blog
	</li>
		<li><strong>Blog Grid Layout:</strong> the page displays blog posts using grid layout options</li>
		<li><strong>Blog Masonry Layout:</strong> this is the page which displays list of post with the masonry layout</li>
		<li><strong>Blog Layout:</strong> the page displays the default WordPress blog page with additional styles </li>
		<li><strong>Landing page:</strong> the home page template</li>
		<li><strong>Portfolio:</strong> portfolio layout page template </li>
		<li><strong>Services page:</strong> the page template created for your services custom post type layout</li>
		<li><strong>Team Page:</strong> the page template created for your team custom post type layout </li>
		<li><strong>Testimonials:</strong> the page template created for testimonials pages</li>
	</ul>
