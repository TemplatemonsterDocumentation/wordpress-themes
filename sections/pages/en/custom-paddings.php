<h3>Setting up custom paddings on page</h3>

<p>
    In case you need to add paddings to the content part of your website, you can choose one of the available options to do that.
</p>
<p>
    In order to set up paddings, navigate to <strong>Cherry Options > Page > Choose paddings for page content</strong> and choose the padding placement type.
</p>

<figure class="img-polaroid"><img src="img/monstroid/page_settings_paddings_options.png" alt=""></figure>

<p>
    Besides that, if you need to create a certain page with one of the padding options,  other than the general type that was set in Cherry Options, you can set it up in the individual page settings.
</p>

<p>
	To implement this, navigate to Pages, select the page on which you want to edit paddings and select the required padding option in block called <strong>Pages > Choose paddings for page content</strong>.
</p>

<figure class="img-polaroid"><img src="img/monstroid/page_settings_paddings.png" alt=""></figure>

<p>
	The default option is Inherit.
</p>

