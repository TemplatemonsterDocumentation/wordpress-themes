<h2 class="item2"><i class="icon-check"></i>Pages</h2>
<p>You can create any number of pages with content. Monstroid includes several page templates to choose from, and you will need to choose the page template that suits your needs.</p>
<h4>Follow These Steps Below To Create A New Page</h4>

<ul>
	<li><strong>Step 1</strong> – Navigate to Pages and click Add New</li>
	<li><strong>Step 2</strong> – Input a new name for your page, then find the Page Attributes box on the right side</li>
	<li><strong>Step 3</strong> – Set your Parent page; it’s usually set to No Parent</li>
	<li><strong>Step 4</strong> – Set your template from the dropdown list. See list of page templates below</li>
	<li><strong>Step 5</strong> - Post Sidebars</li>
	<li><strong>Step 6</strong> - Config Page Setting</li>
	<li><strong>Step 7</strong> - Grid Options</li>
</ul>

		