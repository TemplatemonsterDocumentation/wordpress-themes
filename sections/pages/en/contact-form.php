	<h3>Contact form</h3>
	<p>Simply install the Contact Form 7 plugin, navigate to the "Contact" option on the main dashboard menu, and select "Add New"</p> 
	<figure class="img-polaroid"><img src="img/monstroid/contactform-1.jpg" alt=""></figure>     
	<p>Edit the form fields, your contact email, and customize any of your auto messages. Save the form and then use the shortcode provided in the brown bar at the top to insert your contact form wherever you want it.</p>
