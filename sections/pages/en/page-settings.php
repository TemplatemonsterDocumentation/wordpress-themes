<h4>
	Grid type
</h4>

<figure class="img-polaroid"><img src="img/cherryframework/page_settings_grid_type.png" alt=""></figure>

<p>
    Allows you to configure the container width settings per the following sections: Header, Content, Footer.
</p>

<h4>
	Layout
</h4>

<figure class="img-polaroid"><img src="img/cherryframework/page_settings_layout_type.png" alt=""></figure>

<p>
	Allows you to select the layout type
</p>

<h4>
	Style
</h4>

<figure class="img-polaroid"><img src="img/cherryframework/page_settings_header_style.png" alt=""></figure>

<p>
	Allows you to set up the header background image, background color if required, repeat parameters, position and scroll type.
</p>

