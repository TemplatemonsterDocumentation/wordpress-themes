             <strong>I have customized the template and would like to protect my content by copyright, can I place Copyright 2008, Mycompanyname.com?</strong><br>
              Yes, you can add your copyright to to protect your content.
        </li>
        <li>
             <strong>Can I use graphics from the template we purchased for use in printing?</strong><br>
              Yes, it’s not prohibited to use the template graphics for printed material but the resolution of the templates graphics ( except for logo templates) is not enough for printing. All the templates are designed for 72 dpi resolution while the printing requires 300 dpi resolution.
        </li>
