	<h3>Monstroid dashboard</h3>

	<p>
		On the main dashboard page <strong>Monstroid -> Dashboard</strong>
	</p>

	<figure class="img-polaroid">
	    <img src="img/monstroid/plugins/dashboard_main_updated.png" alt="" style="opacity: 1;"><i class="icon-search"></i><span></span>
	</figure>

	<p>
		You can see the current theme version, in case the new version is available, you will see the following notice: “New Monstroid version available- 1.X.X”.
	</p>

	<figure class="img-polaroid">
	    <img src="img/monstroid/plugins/dashboard_notice.png" alt="" style="opacity: 1;"><i class="icon-search"></i><span></span>
	</figure>

	<p>
		In this case you will see the option to update Monstroid or skip the notice. Besides this, you can see the following links below:
	</p>

	<ol class="index-list">
		<li>
			Monstroid documentation.
		</li>
		<li>
			Quick start guide.
		</li>
		<li>
			Monstroid video tutorials.
		</li>
	</ol>

	<p>
		In order to start updating Monstroid, navigate to <strong>“Monstroid => Data manager”</strong>
	</p>

	<figure class="img-polaroid">
		<img src="img/monstroid/plugins/dashboard_update.png" alt="" style="opacity: 1;"><i class="icon-search"></i><span></span>
	</figure>

	<p class="alert alert-info">
		<span>Attention! The update will be completed successfully only if you have not performed any changes to the theme files, otherwise all the changes will be erased!</span>
	</p>

	<p>
		You can check for the available updates by clicking the Check for Updates button. “Check Update”.</ br> Click the Update button, after this plugin will backup your current theme and update the installed one.
	</p>

	<figure class="img-polaroid">
		<img src="img/monstroid/plugins/dashboard_update_complete.png" alt="" style="opacity: 1;"><i class="icon-search"></i><span></span>
	</figure>

	<p>
		When the update is completed you will see the option to save the backup of the old theme to your computer.
	</p>

	<figure class="img-polaroid">
		<img src="img/monstroid/plugins/dashboard_save_archive.png" alt="" style="opacity: 1;"><i class="icon-search"></i><span></span>
	</figure>

	<p>
		You will also have the possibility to download archives directly from your server at the following location: <strong>/wp-content/uploads/update-backups/</strong>. You can also download the latest Monstroid theme by clicking the <strong>“Download latest Monstroid version”</strong>
	</p>

	<h4>Manual Install</h4>

	<p>
		In case you need to install the theme manually, you will need to enter the license key to receive the updates.
	</p>

	<figure class="img-polaroid">
		<img src="img/monstroid/plugins/dashboard_data_manager.png" alt="" style="opacity: 1;"><i class="icon-search"></i><span></span>
	</figure>

	<p>
		To disable automatic updates click the <strong>“Disable automatic updates” button.</strong>.
	</p>