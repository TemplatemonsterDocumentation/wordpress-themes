					<p>Slides posts have next settings: </p>
					<ul>
						<li><b>Content width</b> - slide content width</li>
						<li><b>Content vertical</b> - </li>
						<li><b>Content Position</b> - slide content position</li>
						<li><b>Content show/hide transition</b> - enable/disable slide animation</li>
						<li><b>Content show/hide duration</b> - slide animation duration</li>
						<li><b>Content show/hide delay</b> - slide animation pause duration</li>
						<li><b>Content wrapper</b> - wrap content into additional block</li>
