<h3>Simple Slider</h3>
<p>This plugin adds a new post type called "Slides." "Slides" posts have additional settings for slides formatting. You
   can work with slides posts in the same way as you would work with regular posts. Slides posts can be arranged into
   categories (sliders) for better navigation.</p>

<p>After the plugin is installed and activated, you will see the Slides List tab in the WordPress dashboard where you
   can work with slides posts and categories. </p>

<p>Slides posts have the following settings: </p>

<ul class="marked-list">
    <li><b>Content width</b> - slide content width</li>
    <li><b>Content vertical</b> - slide content height</li>
    <li><b>Content Position</b> - slide content position</li>
    <li><b>Content show/hide transition</b> - enable/disable slide animation</li>
    <li><b>Content show/hide duration</b> - duration of slide animation</li>
    <li><b>Content show/hide delay</b> - pause duration between slides</li>
    <li><b>Content wrapper</b> - wrap content into additional block.</li>
</ul>

