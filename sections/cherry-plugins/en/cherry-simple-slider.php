			<h3>Simple Slider</h3>
			<p>This plugin adds new post type called "slides". "Slides" posts type have additional settings for slides formatting. You can work with service posts the same way as you would work with regular posts. Portfolio posts can be arranged into categories (sliders) for a better navigation.</p>

			<p>After plugin is installed and activated, you will see Slides List tab in the Wordpress dashboard, where you can work with slides posts and categories. </p>
