	<h3>Cherry Grid Plugin</h3>
	<p>Adds <strong>"Grid"</strong> shortcode and additional layout options to <strong>Posts</strong> and <strong>Pages</strong> screens.</p>
	<figure class="img-polaroid">
			<img src="img/cherryframework/plugins/grid_post_setting.png" alt="" style="opacity: 1;"><i class="icon-search"></i><span></span>
	</figure>
	<p>Testimonials post gains additional settings: </p>
	<ul>
		<li><b>Width</b> - width of the element</li>
		<li><b>Show with image</b> - check if you want to show post images </li>
		<li><b>Excerpt length</b> - post excerpt length</li>
		<li><b>Item background color</b></li>
		<li><b>Item text color</b></li>
		<li><b>CSS class for post item</b></li>
	</ul>
