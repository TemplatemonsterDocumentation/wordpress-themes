<h3>Charts Plugin</h3>

<p>
    The Charts plugin is used to create various charts and diagrams. The plugin's rich collection of diagram and chart
    types enhanced by advanced settings allows you to create a chart that fully fits your requirements.
</p>

<p>
    After the plugin is installed and activated, you will see the Charts tab in the WordPress dashboard where you can
    work with diagrams and charts.
</p>

<p>
    Click <b>"Charts > Add New Chart"</b> to create a new diagram. Enter title and required settings on the chart page.
</p>

<p>
    Four types of diagrams are available: progress bar, pie, doughnut, bar. You can choose the one you need by clicking
    <b>Chart type</b>. Diagram settings depend on the diagram type you have chosen. Let's take a closer look at each
    one.
</p>


<h4>Progress bar</h4>

<h5>Chart settings</h5>

<p>Progress bar has the following settings:</p>


<dl class="inline-term">
    <dt>Progress bar type</dt>
    <dd>progress bar type (radial, horizontal, vertical).</dd>
</dl>

<dl class="inline-term">
    <dt>Inner cutout percentage (radial only)</dt>
    <dd>inner contour size.</dd>
</dl>

<dl class="inline-term">
    <dt>Width</dt>
    <dd>diagram width.</dd>
</dl>

<dl class="inline-term">
    <dt>Height</dt>
    <dd>diagram height.</dd>
</dl>

<h5>Content settings</h5>


<dl class="inline-term">
    <dt>Progress bar template</dt>
    <dd> diagram template.</dd>
</dl>
<dl class="inline-term">
    <dt>Chart icon</dt>
    <dd> diagram icon class.</dd>
</dl>
<dl class="inline-term">
    <dt>Chart icon size</dt>
    <dd> icon size.</dd>
</dl>
<dl class="inline-term">
    <dt>Chart icon color</dt>
    <dd> diagram icon color.</dd>
</dl>


