	<h3>Cherry Style Switcher</h3>

	<p>
		<a href="https://github.com/CherryFramework/cherry-style-switcher">Cherry Style Switcher</a> - plugin lets you switch the skins, select layouts, customize position of info blocks in the header and footer.
	</p>

	<p>
		Open WordPress dashboard, go to “Cherry => Option => Style Switcher”
	</p>

	<p>
		For more information about the settings in the options, described here - <a href="">Style Switcher options</a>
	</p>

	<figure class="img-polaroid">
	    <img src="img/cherryframework/plugins/style-switcher-options.png" alt="" style="opacity: 1;"><i class="icon-search"></i><span></span>
	</figure>

	<p>
		When the plugin is enabled in options, you can set up the "style switcher" options in public version of the site.
	</p>

