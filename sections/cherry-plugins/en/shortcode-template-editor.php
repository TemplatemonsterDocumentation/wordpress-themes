			<h3>Shortcode Template Editor</h3>
			<p>Plugin for unique template of shortcode</p>
			<ol>
				<li>Log into the WordPress dashboard.</li>
				<li>Click <strong>Appearance</strong> tab.</li>
				<li>Click <strong>Shortcodes Templater</strong></li>
			</ol>
			<figure class="img-polaroid"><img src="img/wordpress/1shortcode-editor-panel.png" alt=""></figure>

			<p>If you did not create any shorcodes, you will see welcome message only.</p>
			<figure class="img-polaroid"><img src="img/wordpress/1shortcode-editor-first.png" alt=""></figure>
			<p>First of all choose necessary shortcode and click <strong>Add new</strong></p>
			<figure class="img-polaroid"><img src="img/wordpress/1shortcode-editor-start.png" alt=""></figure>
			<p>Now you can create a new template based on the default one. It will get a name with an ordinal prefix and shortcode name <strong>[Shortcode Name][N]</strong>, e.g. <strong>post_1</strong>.
			Shortcode name will be displayed in the list and in the title of visual editor. Template is physically stored in the directory: \wp-content\themes\cherryframework4\templates\shortcodes\
			</p>
			<figure class="img-polaroid"><img src="img/wordpress/1shortcode-editor-new-template.png" alt=""></figure>
			<p>You can rename, copy and delete the template. While copying <strong>Duplicate</strong> the template, it will get a name with an ordinal prefix (by analogy with the new template) but with the structure of current template instead of default template.</p>
			<figure class="img-polaroid"><img src="img/wordpress/1shortcode-editor-edit.png" alt=""></figure>
			<p>Shortcode template editor allows you to create your own markup, display necessary <strong>(Macros)</strong> and set up display parametres in some of them. For example, clicking <strong>Content</strong> button, you will generate macros <strong>%%CONTENT%%</strong> that will display post content on the website.</p>
			<figure class="img-polaroid"><img src="img/wordpress/1shortcode-editor-making.png" alt=""></figure>
