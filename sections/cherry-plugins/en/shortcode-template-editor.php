				<div class="grid_6">
					<h3>Shortcode Template Editor</h3>
					<p> This plugin allows to create unique shortcodes templates.</p>
					<ol>
						<li>Log into the WordPress dashboard.</li>
						<li>Click the <strong>Appearance</strong> tab.</li>
						<li>Click <strong>Shortcodes Templater</strong>.</li>
					</ol>
				</div>
			</div>
			
			<div class="row">
				<div class="grid_6">
					<figure class="img-polaroid"><img src="img/wordpress/1shortcode-editor-first.png" alt=""></figure>
				</div>
				<div class="grid_6">
					<p>If you did not create any shortcodes, you will see the welcome message only.</p>
				</div>
			</div>

			<div class="row">
				<div class="grid_6">
					<figure class="img-polaroid"><img src="img/wordpress/1shortcode-editor-start.png" alt=""></figure>
				</div>
				<div class="grid_6">
					<p>First, choose the necessary shortcode and click <strong>Add new</strong>.</p>
				</div>
			</div>

			<div class="row">
				<div class="grid_6">
					<a href="img/wordpress/1shortcode-editor-new-template.png" class="pretty_image" rel="prettyPhoto[plugins]">
						<figure class="img-polaroid"><img src="img/wordpress/1shortcode-editor-new-template.png" alt=""></figure>
					</a>
				</div>
				<div class="grid_6">
					<p>Now you can create a new template based on the default one. It will get a name with an ordinal prefix and shortcode name <strong>[Shortcode Name][N]</strong>, e.g. <strong>post_1</strong>.
					The shortcode name will be displayed in the list and in the visual editor title. The template will be physically stored in the \wp-content\themes\cherryframework4\templates\shortcodes\ directory.</p>
				</div>
			</div>

			<div class="row">
				<div class="grid_6">
					<a href="img/wordpress/1shortcode-editor-edit.png" class="pretty_image" rel="prettyPhoto[plugins]">
						<figure class="img-polaroid"><img src="img/wordpress/1shortcode-editor-edit.png" alt=""></figure>
					</a>
				</div>
				<div class="grid_6">
					<p>You can rename, copy and delete the template. While copying, <strong>Duplicate</strong> the template. It will get a name with an ordinal prefix (by analogy with the new template) but with the structure of current template instead of the default template.</p>
				</div>
			</div>

			<div class="row">
				<div class="grid_6">
					<a href="img/wordpress/1shortcode-editor-making.png" class="pretty_image" rel="prettyPhoto[plugins]">
						<figure class="img-polaroid"><img src="img/wordpress/1shortcode-editor-making.png" alt=""></figure>
					</a>
				</div>
				<div class="grid_6">
					<p>Shortcode template editor allows you to create your own markup, display necessary <strong>Macros</strong> and set up display parameters in some of them. For example, clicking the <strong>Content</strong> button, you will generate the <strong>%%CONTENT%%</strong> macro which will display post content on the website.</p>
				</div>
			</div>			
