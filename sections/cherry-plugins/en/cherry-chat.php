			<h3>Cherry Chat</h3>
			<p>Cherry Chat plugin adds live chat application to your WordPress administration panel. Using this live chat you can get 24/7 assistance on working with Cherry Framework and Cherry plugins. </p>	

			<div class="row">
				<div class="grid_6">					
					<figure class="img-polaroid">
						<a href="img/cherryframework/plugins/chat_open.jpg" class="pretty_image" rel="prettyPhoto[plugins]">
							<img src="img/cherryframework/plugins/chat_open.jpg" alt="">
						</a>
					</figure>
				</div>
				<div class="grid_6">
					<p>Live Chat is available in right bottom corner of the WordPress admin panel. </p>
					<p>If you purchased Cherry Framework theme you can use your order ID to contact technical support team and get immediate assistance. </p>
				</div>
			</div>

			<div class="row">
				<div class="grid_6">
					<figure class="img-polaroid">
						<a href="img/cherryframework/plugins/chat_new.jpg" class="pretty_image" rel="prettyPhoto[plugins]">
							<img src="img/cherryframework/plugins/chat_new.jpg" alt="">
						</a>
					</figure>
				</div>
				<div class="grid_6">
					<p>If you do not have an order ID you can also contact live chat support entering your name and email.</p>
					<p>Clicking "Start Chat" button you'll be connected to first available operator. </p>
				</div>
			</div>

			<div class="row">
				<div class="grid_6">
					<figure class="img-polaroid">
						<a href="img/cherryframework/plugins/chat_session.jpg" class="pretty_image" rel="prettyPhoto[plugins]">
							<img src="img/cherryframework/plugins/chat_session.jpg" alt="">
						</a>
					</figure>
				</div>
				<div class="grid_6">
					<p>Talking with chat operator you can also use chat tools: </p>
					<ul>
						<li>Help Center - open online help center where you can view various text and video tutorials;</li>
						<li>Submit a ticket - if your issue can not be resolved in live chat you can use online help desk and submit a ticket; </li>
						<li>Send log - using this link you can have your chat session log sent to your email address;</li>
						<li>Complaint form - used in case you are not satisfied with the quality of chat services;</li>
						<li>Upload file - you may need to send some files to the chat operator to demonstrate your issue;</li>
						<li>Rate Operator - when your chat session is done you can rate chat operator. </li>
					</ul>
				</div>
			</div>

			<div class="row">
				<div class="grid_6">
					<figure class="img-polaroid">
						<a href="img/cherryframework/plugins/chat_disconnect.jpg" class="pretty_image" rel="prettyPhoto[plugins]">
							<img src="img/cherryframework/plugins/chat_disconnect.jpg" alt="">
						</a>
					</figure>
				</div>
				<div class="grid_6">
					<p>If where are some connection issues while you are working in the chat you'll see such notification window. Click "Reconnect" to restart chat or "End chat" to close chat window. </p>					
				</div>
			</div>

			<p>Live Chat can be disabled same as any other WordPress plugin in administration panel > Plugins section. Locate "Cherry Live Chat" plugin and click "Deactivate" button. </p>

