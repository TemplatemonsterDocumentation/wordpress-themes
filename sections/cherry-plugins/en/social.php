<h3>Cherry Social</h3>
<p><a href="https://github.com/CherryFramework/cherry-social" target="_blank">Cherry Social</a> plugin is used to add sharing and follow lists on your website.</p>

<p>Choose social network for sharing</p>

<ul class="marked-list">
    <li><strong>Facebook URL</strong> - Facebook page URL.</li>
    <li><strong>Twitter</strong> - Twitter page URL.</li>
    <li><strong>Google+ URL</strong> - Google+ page URL.</li>
    <li><strong>Pinterest URL</strong> - Pinterest page URL.</li>
    <li><strong>Linkedin URL</strong> - Linkedin page URL.</li>
    <li><strong>Tumblr</strong> - Tumblr page URL</li>
    <li><strong>StumbleUpon</strong> - StumbleUpon page URL.</li>
    <li><strong>Reddit</strong> - Reddit page URL.</li>
</ul>

<p>Title will be used in static item</p>
<p>You can set up the list of social accounts and edit the URL, icon and title.</p>

