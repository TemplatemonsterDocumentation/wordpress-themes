			<h3>Portfolio Plugin</h3>
			<p>This plugin adds new post type called <a href="#portfolio-post">"Portfolio"</a>. "Portfolio" posts type have additional settings for portfolio formatting. You can work with service posts the same way as you would work with regular posts. Portfolio posts can be arranged into categories for a better navigation. </p>

			<p>After plugin is installed and activated, you will see Portfolio tab in the WordPress dashboard, where you can work with portfolio posts and categories.</p>
