			<h3>Portfolio Plugin</h3>
			<p>This plugin adds a new post type called "<a href="#portfolio-post">Portfolio</a>." "Portfolio" posts have additional settings for portfolio formatting. You can work with portfolio posts the same way you would work with regular posts. Portfolio posts can be arranged into categories for better navigation. </p>

			<p>After the plugin is installed and activated, you will see the Portfolio tab in the WordPress dashboard where you can work with portfolio posts and categories.</p>
