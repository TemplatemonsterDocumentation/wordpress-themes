<h3>Cherry Shortcodes</h3>

<p><a href="https://github.com/CherryFramework/cherry-shortcodes" target="_blank">Cherry Shortcodes</a> plugin helps you work with shortcodes (pre-set shortcodes that are used by default). Moreover, modified
   and new shortcodes are described in the
    <a href="index.php?lang=en&section=shortcodes">Shortcodes</a> section.
</p>

<ol class="index-list">
    <li>Log into the WordPress dashboard.</li>
    <li>Click the <strong>Posts</strong> tab.</li>
    <li>Click <strong>Add New</strong>.</li>
</ol>

