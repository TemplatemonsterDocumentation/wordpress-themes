<h3>Cherry Sidebar Manager</h3>

<p><a href="https://github.com/CherryFramework/cherry-sidebar-manager" target="_blank">Cherry Sidebar Manager</a> plugin is used to create custom sidebars with specific sets of widgets.</p>

<p>To add a new sidebar, go to the <b>Appearance > Widgets</b> section and click <strong>Create a new sidebar</strong>.
</p>

<figure class="img-polaroid">
    <img src="img/cherryframework/plugins/sidebar_manager_start.png" alt="" style="opacity: 1;">
</figure>

<figure class="img-polaroid">
    <img src="img/cherryframework/plugins/sidebar_manager_new.png" alt="" style="opacity: 1;">
</figure>

<p>Input <strong>Sidebar name</strong> and <strong>Sidebar description</strong>. Then click <b>Creating Sidebars</b>.
</p>

<figure class="img-polaroid">
    <img src="img/cherryframework/plugins/sidebar_manager_create.png" alt="" style="opacity: 1;">
</figure>
<figure class="img-polaroid">
