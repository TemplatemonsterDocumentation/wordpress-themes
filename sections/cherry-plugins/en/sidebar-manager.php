</figure>

<p>You can arrange your widgets set in your sidebar by adding new ones, changing the order of widgets and removing
   unused ones.</p>

<figure class="img-polaroid">
    <img src="img/cherryframework/plugins/sidebar_manager_new_widget.png" alt="" style="opacity: 1;">
</figure>


<h4>Post/Page plugin settings</h4>

<p>When the Sidebar Manager plugin is active, you will see the following options on the post and page editing
   screens:</p>
<ul>
    <li><b>Main Sidebar</b> - list of available main sidebars</li>
    <li><b>Secondary Sidebars</b> - list of available secondary sidebars</li>
    <li><b>Widgets page</b> - widgets editing screen.</li>
</ul>

