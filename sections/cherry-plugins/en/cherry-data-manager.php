			<h3>Cherry Data Manager</h3>
			<p>Data Manager plugin is used to export/import website data.</p>

			<h4>Export</h4>
			<p>Export is used to transfer website data from one server to another and to create backups. CLick "Tools > Cherry Export"  and then click "Export Content" to use export feature. As the result, you will get an archive with website data.</p>

			<h4>Import</h4>

			<ol>
				<li>Click <strong>Tools > Cherry Import.</strong></li>
				<li>If server settings are correct, you will see loading screen. Open template folder <b>"theme/sample_data" </b> and upload file into Wordpress. You need to choose all files in the folder <b>"theme/sample_data"</b> (hot keys CTRL+A or Command+A), drag and drop them into the into the field.
				</li>

				<li>Upload files located in the sample_data folder including .JSON и .XML. Once files are uploaded, click "Continue Install". <br>
				<div class="box-notification"><span>ATTENTION: .JSON и .XML files are sample demo installation required files. Continue Install button will activate only after you upload .XML и .JSON files.</span></div></li>

				<li>"Continue Install" button will start process of sample data installation. Installation will take some time (depending on the server settings). Please wait till it's done.</li>
				<li><span><strong>Congratulations!</strong> You have successfully installed Cherry WordPress theme and now you can check how it looks like.</span></li>
			</ol>
