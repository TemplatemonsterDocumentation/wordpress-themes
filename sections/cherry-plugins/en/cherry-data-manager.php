			<h3>Cherry Data Manager</h3>
			<p>The Data Manager plugin is used to export/import website data.</p>

			<h4>Export</h4>
			<p>Export is used to transfer website data from one server to another and to create backups. Click "Tools > Cherry Export"  and then click "Export Content" to use the export feature. As a result, you will get an archive with website data.</p>

			<h4>Import</h4> 

			<ol>
				<li>Click <strong>Tools > Cherry Import.</strong></li>
				<li>If server settings are correct, you will see the loading screen. Open the <b>"theme/sample_data" </b> template folder and upload the file into WordPress. You need to choose all files in the <b>"theme/sample_data"</b> folder (hot keys CTRL+A or Command+A), drag and drop them into the field.
				</li>

				<li>Upload files located in the sample_data folder including .JSON and .XML. Once files are uploaded, click "Continue Install." <br>
				<div class="box-notification"><span>ATTENTION: .JSON and .XML files are sample demo installation required files. The Continue Install button will activate only after you upload .XML and .JSON files.</span></div></li>

				<li>"Continue Install" button will start the process of sample data installation. Installation will take some time (depending on the server settings). Please wait till it is done.</li>
				<li><span><strong>Congratulations!</strong> You have successfully installed Cherry WordPress theme and now you can check how it looks.</span></li>
			</ol>
