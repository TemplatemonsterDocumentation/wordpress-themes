			<h3>Testimonials Plugin</h3>
			<p>This plugin adds a new posts type called <a href="#testimonial-post">"Testimonials"</a>. "Testimonials" posts type has additional settings for formatting your website/organization/products testimonials. You can work with service posts the same way as you would work with regular posts.</p>

			<p>After plugin is installed and activated, you will see Testimonials tab in the WordPress dashboard, where you can work with testimonials posts.</p>

