	<h3>Testimonials Plugin</h3>
	<p>This plugin adds a new posts type called "<a href="#testimonial-post">Testimonials</a>." "Testimonials" posts type has additional settings for formatting your website/organization/products testimonials. You can work with testimonials posts in the same way as you would work with regular posts.</p>

	<p>After the plugin is installed and activated, you will see the Testimonials tab in the WordPress dashboard where you can work with testimonials posts.</p>

	<figure class="img-polaroid">
		<img src="img/cherryframework/plugins/testimonials_post-options.jpg" alt="" style="opacity: 1;"><i class="icon-search"></i><span></span>
	</figure>
	<p>Testimonial posts have these settings: </p>
	<ul>
		<li><b>Name</b> - testimonial author name</li>
		<li><b>Email</b> - testimonial author email</li>
		<li><b>URL</b> - testimonial author website URL.</li>
	</ul>
