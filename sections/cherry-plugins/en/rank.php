	<h3>Cherry Rank</h3>

	<p><a href="https://github.com/CherryFramework/cherry-style-switcher">Cherry Rank</a> - plugin is used for show ratings, likes / dislikes, as well as views counter.</p>

	<p>
		On the main dashboard page <strong>â€œCherry => Option => Blog => Additional post meta</strong>" here you can define the settings, <a href="./index.php?project=monstroid&lang=en&section=cherry-options#option-blog">more</a>.
	</p>

<p>With a help of macros you can set up how to show elements in template:</p>
	<ul class="marked-list">
		<li>
			<dl class="inline-term">
				<dt>%%LIKES%%</dt>
				<dd>
					To display the like counter
				</dd>
			</dl>
		</li>
		<li>
			<dl class="inline-term">
				<dt>%%DISLIKES%%</dt>
				<dd>
					To display the dislike counter
				</dd>
			</dl>
		</li>
		<li>
			<dl class="inline-term">
				<dt>%%VIEWS%%</dt>
				<dd>
					To display the view counter
				</dd>
			</dl>
		</li>
		<li>
			<dl class="inline-term">
				<dt>%%RATING%%</dt>
				<dd>
					To display the rating counter
				</dd>
			</dl>
		</li>
	</ul>
