<h3>Cherry Testimonials</h3>
<p><a href="https://github.com/CherryFramework/cherry-testimonials" target="_blank">Cherry Testimonials</a> plugin adds a new posts type called "
    <a href="./index.php?lang=en&section=posts-categories#testimonial-post">Testimonials</a>
   ." "Testimonials" posts type has additional settings for formatting your website/organization/products testimonials.
   You can work with testimonials posts in the same way as you would work with regular posts.
</p>

<p>After the plugin is installed and activated, you will see the Testimonials tab in the WordPress dashboard where you
   can work with testimonials posts.</p>

<p>Testimonial posts have these settings: </p>
<ul class="marked-list">
    <li><b>Name</b> - testimonial author name</li>
    <li><b>Email</b> - testimonial author email</li>
    <li><b>URL</b> - testimonial author website URL.</li>
</ul>

