			<h3>Services Plugin</h3>
			<p>This plugin ads a new post type called "services". Services posts have additional settings to form attractive blocks for service description. You can work with service posts the same way as you would work with regular posts. Service posts can be arranged into categories for a better navigation.</p>

			<p>After plugin is installed and activated, you will see Service tab in the WordPress dashboard, where you can work with services posts and categories.</p>
