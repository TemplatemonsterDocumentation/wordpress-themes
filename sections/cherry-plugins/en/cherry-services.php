<h3>Services Plugin</h3>
<p>
    This plugin adds a new post type called "services." Services posts have additional settings to form attractive
    blocks
    for service description. You can work with service posts the same way you would work with regular posts. Service
    posts can be arranged into categories for better navigation.
</p>

<p>
    After the plugin is installed and activated, you will see the Service tab in the WordPress dashboard where you can
    work with services posts and categories.
</p>

<p>Services posts have the following settings: </p>

<ul class="marked-list">
    <li><dl class="inline-term"><dt>Font icon CSS class</dt> <dd>the CSS class of service description block icon</dd></li>
    <li><dl class="inline-term"><dt>Service features description</dt> <dd>brief description of the service</dd></li>
    <li><dl class="inline-term"><dt>Price</dt> <dd>price</dd></li>
    <li><dl class="inline-term"><dt>URL to order this service</dt> <dd>service URL</dd></li>
    <li><dl class="inline-term"><dt>Is featured service</dt> <dd>is this service popular?</dd></li>
</ul>

