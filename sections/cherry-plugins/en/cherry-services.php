	<h3>Services Plugin</h3>
	<p>This plugin adds a new post type called "services." Services posts have additional settings to form attractive blocks for service description. You can work with service posts the same way you would work with regular posts. Service posts can be arranged into categories for better navigation.</p>

	<p>After the plugin is installed and activated, you will see the Service tab in the WordPress dashboard where you can work with services posts and categories.</p>
	
	<figure class="img-polaroid">
		<img src="img/cherryframework/plugins/services_post-options.jpg" alt="" style="opacity: 1;"><i class="icon-search"></i><span></span>
	</figure>
	<p>Services posts have the following settings: </p>
	<ul>
		<li><b>Font icon CSS class</b> - the CSS class of service description block icon </li>
		<li><b>Service features description</b> - brief description of the service</li>
		<li><b>Price</b> - price</li>
		<li><b>URL to order this service</b> - service URL</li>
		<li><b>Is featured service</b> - is this service popular?</li>
	</ul>
