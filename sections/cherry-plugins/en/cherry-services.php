					<p>Services posts has next settings: </p>
					<ul>
						<li><b>Font icon CSS class</b> - service description block icon CSS class</li>
						<li><b>Service features description</b> - service brief description</li>
						<li><b>Price</b> - price</li>
						<li><b>URL to order this service</b> - service URL</li>
						<li><b>Is featured service</b> - is this service popular?</li>
