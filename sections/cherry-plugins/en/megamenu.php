<h3>Cherry Mega Menu</h3>

<p><a href="https://github.com/CherryFramework/cherry-mega-menu" target="_blank">Cherry Mega Menu</a> plugin allows you to create multi-column menu drop-downs. It also lets you display necessary information in the
   main menu.</p>

<div class="alert alert-warning">If the WPML plugin is used, you should turn off mega menu caching. The Mega Menu plugin has a caching option: Cherry Options -> Optimization -> Mega menu caching enabled -> Off.

 </div>

<figure class="img-polaroid">
    <img src="img/cherryframework/plugins/megamenu_item.jpg" alt="" style="opacity: 1;"><i
        class="icon-search"></i><span></span>
</figure>

<p>After the plugin is installed and activated, you can navigate to "Appearance > Menus" in the WordPress dashboard.
   Hover over the menu tab and click the "Mega Menu" tab. </p>

<figure class="img-polaroid">
    <img src="img/cherryframework/plugins/megamenu_step1.jpg" alt="" style="opacity: 1;"><i
        class="icon-search"></i><span></span>
</figure>

<p>Here you can work with Mega Menu. </p>

<p>Click "Enable Mega Menu for current item" to activate the Mega Menu for a specific menu item.</p>


<h4>Mega Menu</h4>

<p>You can place necessary widgets in the drop down block of the current menu item in the Mega Menu section. Each widget
   can have its own width and height.</p>

<figure class="img-polaroid">
    <img src="img/cherryframework/plugins/megamenu_step4.jpg" alt="" style="opacity: 1;"><i
        class="icon-search"></i><span></span>
</figure>


<h4>Settings</h4>

<p>Here you can find menu item settings. </p>

<p>A menu item has the following settings: </p>
<ul class="marked-list">
    <li><b>Group sub items to columns</b> - group sub-menu items into columns</li>
    <li><b>Columns number</b> - number of columns in megamenu</li>
    <li><b>Submenu position</b> - sub-menu position</li>
    <li><b>Item width</b> - width of megamenu for different resolutions</li>
    <li><b>Hide submenu on mobile</b> - enable/disable megamenu on mobile devices.</li>
</ul>

