<h3>Cherry Shortcodes</h3>

<p>It is a plugin that helps you to work with shortcodes (pre-set shortcodes are used by default). Moreover, modified
   and new shortcodes are described in
    <a href="index.php?lang=en&section=shortcodes">Shortcodes</a>.
</p>

<ol class="index-list">
    <li>Log into the WordPress dashboard.</li>
    <li>Click the <strong>Posts</strong> tab.</li>
    <li>Click <strong>Add New</strong>.</li>
</ol>

