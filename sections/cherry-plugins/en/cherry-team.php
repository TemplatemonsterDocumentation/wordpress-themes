			<h3>Team Plugin</h3>
			<p>This plugin adds new post type called <a href="#team-post">"Team"</a>. "Team" posts type have additional settings your team members posts formatting. You can work with team posts the same way as you would work with regular posts. Service posts can be arranged into categories for a better navigation.</p>

			<p>After plugin is install and activated you will see Team tab in the WordPress dashboard, where you can work with posts and categories. </p>
