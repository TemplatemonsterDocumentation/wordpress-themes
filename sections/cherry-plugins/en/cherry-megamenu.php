			<h3>Mega Menu</h3>
			<p>This plugin allows you to create multi-column menu drop-downs. Plugin also allows to display necessary information in the main menu.</p>

			<p>After plugin is installed and activated, you can navigate to "Appearance > Menus" in the Wordpress dashboard. Hover the menu tab and click Mega Menu icon. </p>
			<figure class="img-polaroid">
				<a href="img/cherryframework/plugins/megamenu_item.jpg" class="pretty_image" data-gal="prettyPhoto">
					<img src="img/cherryframework/plugins/megamenu_item.jpg" alt="" style="opacity: 1;"><i class="icon-search"></i><span></span>
				</a>
			</figure>

			<p>Here you can work with MegaMenu. </p>

			<figure class="img-polaroid">
				<a href="img/cherryframework/plugins/megamenu_step1.jpg" class="pretty_image" data-gal="prettyPhoto">
					<img src="img/cherryframework/plugins/megamenu_step1.jpg" alt="" style="opacity: 1;"><i class="icon-search"></i><span></span>
				</a>
			</figure>

			<p>Click "Enable Mega Menu for current item" to activate MegaMenu for specific menu item.</p>

			<h4>Mega Menu</h4>
			<p>You can place necessary widgets in the srop down block of current menu item in the MegaMenu section. Each widget can have its own width and height.</p>
			<figure class="img-polaroid">
				<a href="img/cherryframework/plugins/megamenu_step4.jpg" class="pretty_image" data-gal="prettyPhoto">
					<img src="img/cherryframework/plugins/megamenu_step4.jpg" alt="" style="opacity: 1;"><i class="icon-search"></i><span></span>
				</a>
			</figure>

			<h4>Settings</h4>
			<p>Here you can find menu item settings. </p>
			<figure class="img-polaroid">
				<a href="img/cherryframework/plugins/megamenu_step2.jpg" class="pretty_image" data-gal="prettyPhoto">
					<img src="img/cherryframework/plugins/megamenu_step2.jpg" alt="" style="opacity: 1;"><i class="icon-search"></i><span></span>
				</a>
			</figure>
			<p>Menu item has next settings: </p>
			<ul>
				<li><b>Group sub items to columns</b> - group sub-menu items into columns</li>
				<li><b>Columns number</b> - number of columns in megamenu</li>
				<li><b>Submenu position</b> - sub-menu position</li>
				<li><b>Item width</b> - width of megamenu for different resolutions</li>
				<li><b>Hide submenu on mobile</b> - enable/disable megamenu on mobile devices</li>
			</ul>

			<h4>Media</h4>
			<p>It allows you to add an icon to each menu item. In this section you can choose an icon for menu item, choose arrow style, hide menu text or arrow.</p>
