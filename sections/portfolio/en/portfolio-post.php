<h3>Working with the portfolio post types</h3>

<p>
    Cherry Portfolio is based on custom post type - Portfolio. To have the portfolio post format available, Cherry
    Portfolio plugin should be installed.
</p>
<p>
    To add a new Portfolio post, click "<b>Add New</b>" in the portfolio section in the WordPress admin panel.
</p>

<figure class="img-polaroid"><img src="img/wordpress/folio_panel.png" alt=""></figure>

<p>
    As Portfolio posts are a variation of default WordPress post, creating new posts should not be a problem. You can
    <a href="http://codex.wordpress.org/Post_Types#Custom_Types"> learn more</a>
    about WordPress custom posts.
</p>

<ol class="index-list">
    <li>Fill out all necessary fields (<strong>title</strong>, <strong>content</strong>).</li>
    <li>Upload an image using the <strong>Featured Image</strong> option (click<strong> Set featured image</strong>).
    </li>
    <li>Click<strong> Publish</strong>.</li>
</ol>

<figure class="img-polaroid"><img src="img/wordpress/folio.png" alt=""></figure>

<h5>Choosing portfolio post format.</h5>

<p>You can use the following post types in portfolio post:</p>

<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Image</dt>
            <dd>default gallery post type. Displays <em>post image</em> only.</dd>
        </dl>
        <figure class="img-polaroid"><img src="img/wordpress/portfolio_post_type_format_image.png" alt=""></figure>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Slideshow</dt>
            <dd>gallery project is displayed in a slideshow. This gallery post type can contain unlimited number of images.</dd>
        </dl>

    </li>
    <li>
        <dl class="inline-term">
            <dt>Gallery</dt>
            <dd>post images are displayed in a grid gallery. This gallery post type can contain unlimited images.</dd>
        </dl>
        <figure class="img-polaroid"><img src="img/wordpress/portfolio_post_type_format_gallery.png" alt=""></figure>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Video</dt>
            <dd>you can add video from any video hosting like YouTube, Vimeo, etc using this gallery
                post type.</dd>
        </dl>
        <figure class="img-polaroid"><img src="img/wordpress/portfolio_post_type_format_video.png" alt=""></figure>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Audio</dt>
            <dd>this gallery post type allows to add an audio file.</dd>
        </dl>
        <figure class="img-polaroid"><img src="img/wordpress/portfolio_post_type_format_audio.png" alt=""></figure>
    </li>
</ul>
