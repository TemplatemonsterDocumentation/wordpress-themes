<h3>Typography</h3>

<h4>Icon</h4>

<p>Used to add text icons to your post content. </p>
<ul class="marked-list">
    <li><strong>icon:</strong> – filed to add an icon, icon can be added through media manager and icon picker</li>
    <li><strong>icon size:</strong> – icon size (can be used with text icons only)</li>
    <li><strong>icon color:</strong> – icon color</li>
    <li><strong>class:</strong> – shortcode custom class</li>
</ul>
<pre class="prettyprint">[cherry_icon icon="icon: fa fa-headphones" color="#dadada" class="icon-class"]</pre>

