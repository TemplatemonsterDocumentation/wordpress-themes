<h4>Title Box</h4>

<p>Displays double title: main and secondary titles of the text.</p>
<ul class="marked-list">
    <li><strong>title:</strong> – title content</li>
    <li><strong>subtitle:</strong> – subtitle content</li>
    <li><strong>icon:</strong> – filled to add an icon, icon can be added through media manager and icon picker</li>
    <li><strong>icon size:</strong> – icon size (can be used with text icons only)</li>
    <li><strong>icon color:</strong> – icon color</li>
    <li><strong>title color:</strong> – title color</li>
    <li><strong>subtitle color:</strong> – subtitle color</li>
    <li><strong>class:</strong> – shortcode custom class</li>
</ul>
