<h4>Button</h4>

<ul class="marked-list">
    <li><strong>text:</strong> – button text</li>
    <li><strong>link:</strong> – button link</li>
    <li><strong>style:</strong> – button color</li>
    <li><strong>size:</strong> – button size</li>
    <li><strong>display:</strong> – button display style</li>
    <li><strong>border radius:</strong> – radius of button rounded corners</li>
    <li><strong>centered:</strong> – button alignment</li>
    <li><strong>fluid:</strong> – responsive setting</li>
    <li><strong>fluid Position:</strong> – button alignment in responsive layouts</li>
    <li><strong>icon:</strong> – filled to add an icon, icon can be added through media manager and icon picker</li>
    <li><strong>icon Position:</strong> – position of icon relative to button</li>
    <li><strong>description:</strong> – short text description under the button</li>
    <li><strong>background:</strong> – button background color</li>
    <li><strong>text color:</strong> – button text color</li>
    <li><strong>min width:</strong> – minimum button width</li>
    <li><strong>target:</strong> – defines whether button link will open in the new or in the parent window (tab)</li>
    <li><strong>rel attribute:</strong> – rel attribute defines connection between current document and linked document
                                        defined by the href attribute
    </li>
    <li><strong>hover animation:</strong> – hover animation type</li>
    <li><strong>class:</strong> – shortcode custom class</li>
</ul>
