<h4>Dropcap</h4>

<p>Displays dropcap in the text block</p>
<ul class="marked-list">
    <li><strong>Text size:</strong> – text size</li>
    <li><strong>text color:</strong> – text color</li>
    <li><strong>background Color:</strong> – text font color</li>
    <li><strong>align:</strong> – align settings</li>
    <li><strong>shape:</strong> – shape settings</li>
    <li><strong>border:</strong> – set of options (width, style and color) to create a border</li>
    <li><strong>class:</strong> – shortcode custom class</li>
</ul>
