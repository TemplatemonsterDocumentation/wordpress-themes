<h4>Horizontal Rule</h4>

<p>Used to add thematic breaks to the post content.</p>
<ul class="marked-list">
    <li><strong>line height:</strong> – separator height</li>
    <li><strong>line color:</strong> – color</li>
    <li><strong>indent before:</strong> – top indent</li>
    <li><strong>indent after:</strong> – bottom indent</li>
    <li><strong>class:</strong> – shortcode custom class</li>
</ul>
<pre
    class="prettyprint">[cherry_hr height="1" color="#dddddd" indent_top="20" indent_bottom="20" class="rule-class"]</pre>

