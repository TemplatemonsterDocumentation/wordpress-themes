	<h4>Google map</h4>			

	<p>Displays animated counter</p>

	<ul class="marked-list">
		<li><strong>address:</strong> – city</li>
		<li><strong>latitude geographical coordinates:</strong> – latitude value</li>
		<li><strong>longitude geographical coordinates:</strong> – longitude value</li>
		<li><strong>map zoom level:</strong> – initial map zoom level (required value)</li>
		<li><strong>scroll wheel:</strong> – enable/disable srollwheel zooming on the map</li>
		<li><strong>map Style:</strong> – map style</li>
		<li><strong>map height:</strong> – map height</li>
		<li><strong>pan control:</strong> – enable/disable map navigation</li>
		<li><strong>zoom control:</strong> – enable/disable map zooming</li>
		<li><strong>draggable map:</strong> – enable/disable dragging map</li>
		<li><strong>marker source:</strong> – marker image (use media manager to add image)</li>
		<li><strong>class:</strong> – shortcode custom class</li>
	</ul>

