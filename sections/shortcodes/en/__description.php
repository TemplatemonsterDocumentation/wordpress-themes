<h2 class="item6"><i class="icon-file"></i> Shortcodes</h2>
		<p>Shortcodes is a powerful tool in content management <a href="http://codex.wordpress.org/Shortcode">(learn more)</a>. They are very simple in usage. First of all you need to switch to visual editor by clicking <strong>Visual</strong>.</p>
		<figure class="img-polaroid"><img src="img/wordpress/shorcode_ultimate_open.png" alt=""></figure>
		<p>Click <strong>Insert shortcode</strong> button. Next, choose proper shortcode.</p>

		<figure class="img-polaroid"><img src="img/wordpress/shorcode_ultimate_board.png" alt=""></figure>
		<p>All shotcodes can be divided into next groups:</p>
		<ul>
			<li><i class="icon-angle-right"></i><strong>Box</strong> - used to display containers and elements of column formatting and to display grid.</li>
			<li><i class="icon-angle-right"></i><strong>Content</strong> - elements of interface formatting. <strong> </strong>-</li>
			<li><i class="icon-angle-right"></i><strong>Media</strong> - additional content elements.</li>
			<li><i class="icon-angle-right"></i><strong>Gallery</strong> - additional content elements.</li>
			<li><i class="icon-angle-right"></i><strong>Data</strong> - additional content elements.</li>
			<li><i class="icon-angle-right"></i><strong>Other</strong> - subsidiary elements of content advanced management.</li>
		</ul>