<h2>Shortcodes</h2>

<h5>
    Shortcodes is a powerful tool in content management (
    <a href="http://codex.wordpress.org/Shortcode">learn more</a>
    ). They are very simple in usage. First of all you need to switch to visual editor by clicking
    <strong>Visual</strong>.
</h5>

<p>Click <strong>Insert shortcode</strong> button. Next, choose proper shortcode.</p>
<figure class="img-polaroid"><img src="img/wordpress/shorcode_ultimate_open.png" alt=""></figure>

<p>All shotcodes can be divided into the following groups:</p>

<ul class="marked-list">
    <li><strong>Box</strong> - used to display containers and elements of column formatting and to display grid.</li>
    <li><strong>Content</strong> - elements of interface formatting.</li>
    <li><strong>Media</strong> - additional content elements.</li>
    <li><strong>Gallery</strong> - additional content elements.</li>
    <li><strong>Data</strong> - additional content elements.</li>
    <li><strong>Other</strong> - subsidiary elements of content advanced management.</li>
</ul>

