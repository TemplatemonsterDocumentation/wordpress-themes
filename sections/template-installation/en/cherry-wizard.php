<article id="cherry-wizard">
			<h3>Automatic installation using Cherry Wizard</h3>
			<p>Cherry Wizard is a plugin developed to provide easy Cherry Framework themes installation to our customers. </p>
			<figure class="img-polaroid"><img src="img/wordpress/plugins_add_new.png" alt=""></figure>
			<p class="muted text-center"></p>
			<ol>
				<li>Log into the WordPress dashboard.</li>
				<li>Open the <strong>Plugins</strong> tab.</li>
				<li>Click <strong>Add new</strong>.</li>
				<li>Click the "Upload Plugin" button.</li>
				<li>Download <a href="#">Cherry Wizard</a>.</li>
				<li>Upload the downloaded zip archive to the WordPress admin panel and click the "Install" button.</li>
				<!-- <li>Enter "Cherry Wizard" into the "Search plugin" field.</li> -->
				<!-- <li>After you get results of the search, click <strong>Install Now</strong> and install the plugin.</li> -->
			</ol>
			<p>When the installation is done, activate the plugin in the Plugins section. </p>
			<figure class="img-polaroid"><img src="img/wordpress/1Activate.png" alt=""></figure>
			<p>Click <strong>Activate</strong> under the "Cherry Wizard" plugin. Click <b>Begin installation</b> in the notification box that has appeared. </p>

			<figure class="img-polaroid"><img src="img/wordpress/1License_input.png" alt=""></figure>
			<ol>
				<li>Enter the name of the theme in the <strong>Theme Name</strong> field.</li>
				<li>Enter the activation key in the <strong>Activation key</strong> field.</li>
				<li>If you did not purchase the theme, you can choose <strong><u>try a Demo theme</u></strong> to test the demo theme.</li>
			</ol>
		</article>

		<article id="installing-framework-pligins">
			<h4>Downloading and installing framework and plugins</h4>
			<p>Cherry Wizard will automatically install all required plugins. In case there are incompatibilities with server recommended settings, you will see the following notification:</p>
			<figure class="img-polaroid"><img src="img/wordpress/1warning_notice.png" alt=""></figure>
			<p>This message shows your current hosting server settings and recommended ones. Please contact your hosting to make your server compatible with Cherry Plugins. </p>
			<p>You can check the installation details of each plugin and component during the installation process.</p>
			<figure class="img-polaroid"><img src="img/wordpress/1Framework-install-complete.png" alt=""></figure>
			<p>When the installation is complete, you will be automatically redirected to the next step.</p>
		</article>

