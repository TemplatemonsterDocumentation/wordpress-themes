<h3>Adding footer widgets</h3>
<ol class="index-list">
    <li>
        To add the widgets, Navigate to Appearance > Widgets.
    </li>
    <li>
        You will see Footer widget sections on the right side. Your widgets are located
        on the left side.
    </li>
    <li>
        Simply drag and drop the widgets you want from the left side into the sliding bar
        widget sections on the right side.
    </li>
</ol>