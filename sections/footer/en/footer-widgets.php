	<h3>Adding footer widgets</h3>
	<ul>
		<li><strong>Step 1</strong> – To add widgets, Navigate to Appearance > Widgets.</li>
		<li><strong>Step 2</strong> – You will see Footer widget sections on the right hand side. Your widgets are located on the left hand side.</li>
		<li><strong>Step 3</strong> – Simply drag and drop the widgets you want from the left side into the sliding bar widget sections on the right side.</li>
	</ul>
