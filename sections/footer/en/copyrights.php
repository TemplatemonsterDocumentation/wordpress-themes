<h3>Copyrights</h3>
<p>Settings for the website footer section.</p>


<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Footer background</dt>
            <dd>
                Footer background settings. You can select background color, upload footer background image, set its
                background position, attachment and repeat.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Typography</dt>
            <dd>
                Typography settings for footer text.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Grid type</dt>
            <dd>
                Select the layout pattern for website footer. Wide layout will fit window width. Boxed layout will have
                fixed
                width.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Boxed width</dt>
            <dd>Header section width if boxed layout is active. Should not be more than the Grid -> Container width
                value.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Footer Info text</dt>
            <dd>
                Set custom text for Footer area.
            </dd>
        </dl>
    </li>
</ul>


