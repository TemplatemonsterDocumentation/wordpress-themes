			<h3>What is WordPress CMS?</h3>
			<p>WordPress is a free and open-source blogging tool and a content management system (CMS) based on PHP and MySQL. With its help you can create websites and powerful on-line applicationand requires almost no technical skills or knowledge to administrate.  Due to lots of its features (such as ease of use and flexibility) Wordpress has become the most popular platform for website development.<a href="http://wordpress.org/about/" target="_blank">Learn More</a></p>
		</div>

		<div id="wordpress_template_desc">
			<h3>What is Wordpress Template?</h3>
			<p>Wordress template is a theme for Wordpress CMS platform. In other words, you can easily change your website appearance simply installing new Wordpress template in a few easy steps. Despite its simplicity, Wordpress template contains all necessary source files that can be altered the way you need.</p>
