			<h3>Preparation</h3>
			<p>Prior to install Wordpress website, you need to get fully prepared. It is recommended to get next aspects covered:</p>

				<h4>Software</h4>
				<p>Before you even start working with Wordpress template, we recommend you to download required software. You can check required software on the template preview page.<br>
				Requirements can alter from template to template, so we will list the most important ones:</p>
				<ol>
					<li>First of all you'll need correct software to extract file from the password protected archive sources_#########.zip. You can use WinZip 9 or a higher version (if you have Windows OS) or Stuffit Expander 10 or a higher version (if you have Mac OS).</li>
					<li>You might also need Adobe Photoshop. It is used to edit source .PSD files in case you need to change the graphic design and images of the template.</li>
					<li>For editing source code of the template you can use code editors like Adobe Dreamweaver, Notepad++, Sublime Text, etc.</li>
					<li>To upload files to hosting server you will need FTP manager like Total Commander, FileZilla, CuteFTP, etc.</li>
				</ol>

					<h4>Hosting</h4>
					<p>As WordPress CMS is PHP/MySQL platform, you need to have hosting server prepared.</p>
					<p>In case you already have hosting server, you need to check whether it is compatibility with <a href="http://wordpress.org/about/requirements/" target="_blank"> WordPress hosting requirements </a> whether you can host Wordpress website with it.</p>
					<p>You can also install Wordpress on your PC or laptop using local server. You can use next software to create local server <strong>WAMP</strong>, <strong>AppServ</strong>, <strong>MAMP</strong>, etc. All of them support Wordpress and can be installed as a regular program.</p>
