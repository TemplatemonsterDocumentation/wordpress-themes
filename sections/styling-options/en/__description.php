<h2>Styling options</h2>

<p>Monstroid  has an advanced Theme Options panel that is loaded within Cherry Options.</p> 
<p>We have the options organized into logical tabbed sections, and each option has a description of what it will do on the front end. There are too many options to go over in these docs but please take some time and navigate through each tab to see what we offer. Go to Cherry > Options in your WordPress admin to access the theme options panel. See below for screenshots of our options panel. </p>

