	<h3>Setting up the StickUp menu</h3>
	<p>In order to enable the StickUp menu, you just need to navigate to:</p>      
	<ul>
		<li><strong>Step 1</strong> – Cherry > Options</li>
		<li><strong>Step 2</strong> – Header and scroll down to the Sticky selector area</li>
		<li><strong>Step 3</strong> – Use the drop down list to choose the StickUp menu</li>
	</ul>
