<h3>Setting up the StickUp menu</h3>

<p>In order to enable the StickUp menu, you just need to navigate to:</p>

<ol class="index-list">
    <li>Cherry > Options</li>
    <li>Header and scroll down to the Sticky selector area</li>
    <li>Use the drop down list to choose the StickUp menu</li>
</ol>

