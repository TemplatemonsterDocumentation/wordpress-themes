<h3>Adding the logo and favicon</h3>

<p>
    Monstroid requires that you upload your logo as an image file. The logo can be of any size and can fill up the full
    content area. Monstroid is also retina ready so you can upload a Retina favicon which will use the following
    dimensions: 152x152 px.
</p>

<figure class="img-polaroid"><img src="img/monstroid/favicon.jpg" alt=""></figure>

<p>To insert your logo, follow the steps below.</p>

<ol class="index-list">
    <li>Navigate to Cherry > Header > Logo to access the logo options.</li>
    <li>You will see two logo upload options. Click Choose media button and select your logo</li>
</ol>

