<h2>WPML configuration</h2>

<h5>WPML consists of several modules. The basic module turns WordPress sites multilingual. Other modules help with an enhanced translation process, string translation, media translation and other functions.</h5>
    
<p>You can learn about the different modules in the <a href="http://wpml.org/documentation/wpml-core-and-add-on-plugins/" target="_blank"><strong>WPML Core and Addons</strong></a> page.</p>
    
<p>After you enable WPML, you’ll see a Multilingual Widget in the Admin Dashboard. This widget provides access to the different functions in WPML. You can also access them through the <strong>WPML menu</strong>.</p>

<ul class="marked-list">
	<li><a href="http://wpml.org/documentation/getting-started-guide/language-setup/" target="_blank">Languages</a> – Determine the site’s languages and control how different language contents are organized.</li>
	<li><a href="http://wpml.org/documentation/getting-started-guide/theme-localization/" target="_blank">Theme and plugins localization</a> – Choose between the traditional .mo files or using WPML’s String Translation.</li>
	<li><a href="http://wpml.org/documentation/getting-started-guide/site-navigation/" target="_blank">Navigation</a> – Control the appearance of site-wide navigation based on WordPress pages.</li>
	<li><a href="http://wpml.org/documentation/getting-started-guide/sticky-links/" target="_blank">Sticky links</a> – This is an essential function for running full websites with WordPress. It guarantees that links between pages can never break.</li>
</ul>

