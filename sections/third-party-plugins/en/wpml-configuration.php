<h4>Translating Site Content</h4>

<p>WPML creates different posts, pages and custom types for translations and connects them together. This means that every page has its own title, body and meta-fields.</p>
    
<p>WPML offers two ways for translating content:</p>
<ul class="marked-list">
	<li>Manually, by clicking on the + icons</li>
	<li>Using WPML’s Translation Management module</li>
</ul>
        
