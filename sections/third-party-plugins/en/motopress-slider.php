	<h3>MotoPress Slider Plugin</h3>
	<p><a href="http://www.getmotopress.com/blog/responsive-motopress-slider-for-wordpress/" target="_blank">MotoPress Slider</a> is an easy to use and flexible solution for building compelling sliders with beautiful slides. It focuses on simplicity and functionality and offers all necessary tools to stand out among others. You can build your WordPress slider in two minutes and enjoy it from any size screen, as it looks great on any device. </p>
	<p class="alert alert-warning">This instruction is for templates with MotoPress Slider included. If you purchased MotoPress Slider as an additional offer .json file will not be present in the template package. </p> 

	<h4>To make MotoPress Slider look like our live demo, please follow the instructions below:</h4>
	<ol>
		<li>Install and activate MotoPress Slider plugin.</li>
		<li>Go to <b>Dashboard -&gt; MotoPress Slider -&gt;</b> and press the button <b>"Import &amp; Export"</b>.</li>
		<li>Select MotoPress Slider Export <b>.json</b> file that you can find in your template package.</li>
		<li>After importing the settings of the slider, the installation is complete.</li>
	</ol>

