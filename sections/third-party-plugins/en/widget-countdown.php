<h3>Countdown plugin</h3>

<p>WordPress <a href="https://wordpress.org/plugins/homepage-pop-up/" target="_blank">Countdown plugin</a> is a nice tool to create and insert countdown timers into your posts/pages and widgets. </p>

<p>Our WordPress Countdown plugin allows website owners to create nice and functional Countdown timers just in a few minutes and insert them in the posts/pages and widgets. </p>
	
<h4>Installing WordPress Countdown plugin</h4>

<ol class="index-list">
	<li><p>1. Log in to your administration panel.</p></li>
	<li><p>2. Go to Plugins page, click Add New, then click "Upload Plugin".</p></li>
	<li><p>3. Click "Choose file" then select the WordPress Countdown plugin zip file.</p></li>
	<li><p>4. Install and activate our WordPress Countdown plugin.</p></li>
	<li><p>5. You can add Countdown timer from your pages/posts and widgets.</p></li>
</ol>
