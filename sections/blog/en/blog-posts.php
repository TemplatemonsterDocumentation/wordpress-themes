	<h3>Creating blog posts</h3>
	<h4>Quick Edit</h4>
	<p>Navigate to Posts in your WordPress admin.   <br>
	When hovering each title of each post, it will show actions: Edit, Quick Edit, Trash, View.</p>
	<p>Follow the Steps Below To Create A Blog Post:</p>

	<ul>
		<li><strong>Step 1</strong> – Navigate to Posts in your WordPress admin.</li>
		<li><strong>Step 2</strong> – Click on Add New to make a new post. Create a title and insert your post content in the editing field.</li>
		<li><strong>Step 3</strong> – Choose format of post from the right side. Default is Standard.</li>
		<li><strong>Step 4</strong> – Add Categories from the right side. To assign it to the post, check the box next to the Category name.</li>
		<li><strong>Step 5</strong> – Add Tags from the right side. Type the name of the tag in the field, separate multiple tags with commas.</li>
		<li><strong>Step 6</strong> – To add Featured image, click Featured Image Box, select an image and click “Set Featured Image”.</li>
		<li><strong>Step 7</strong> – Once you are finished, click Publish to save the post.</li>
	</ul>

