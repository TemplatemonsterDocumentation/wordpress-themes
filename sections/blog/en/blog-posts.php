<h3>Creating blog posts</h3>

<h4>Quick Edit</h4>

<p>
    Navigate to Posts in your WordPress admin. <br>
    When hovering on each title of each post, it will show the following actions: Edit, Quick Edit, Trash, View.
</p>

<p>Follow the Steps Listed Below To Create A Blog Post:</p>

<ol class="index-list">
    <li>Navigate to Posts in your WordPress admin.</li>
    <li>Click the Add New button to create a new post. Create a title and insert your post content in
        the editing field.
    </li>
    <li>Choose the post format on the right side. Default is Standard.</li>
    <li>Add Categories from the right side. To assign it to the post, check the box next to
        the Category name.
    </li>
    <li>Add Tags from the right side. Type the name of the tag in the field, separate multiple
        tags with commas.
    </li>
    <li>To add the Featured image, click Featured Image Box, select an image and click “Set
        Featured Image”.
    </li>
    <li>Once you are finished, click Publish to save the post.</li>
</ol>

