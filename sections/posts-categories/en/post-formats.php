		<article id="post-formats">
			<h3>Post Formats</h3>
			<p>Post formats are used to customize post available options and markup. You can choose post format at the post editing screen.</p>
			<figure class="img-polaroid"><img src="img/wordpress/post_format.png" alt=""></figure>
			<p>You can check detailed information post formats at <a href="http://codex.wordpress.org/Post_Formats">http://codex.wordpress.org/Post_Formats</a>.</p> 
			<p>Check the list of available posts formats with description and settings below.</p>

			<article id="post_format_standard">
				<h4>Standard Post Format</h4>
				<p>Standard post format has the following fields:</p>
				<ul>
					<li><i class="fa fa-angle-right"></i> <strong>Post title</strong> - post title</li>
					<li><i class="fa fa-angle-right"></i> <strong>Post content</strong> - post content</li>
					<li><i class="fa fa-angle-right"></i> <strong>Categories</strong> - post category(ies)</li>
					<li><i class="fa fa-angle-right"></i> <strong>Tags</strong> - post tags</li>
					<li><i class="fa fa-angle-right"></i> <strong>Featured Image</strong> - post featured image</li>
					<li><i class="fa fa-angle-right"></i>other fields that can be enabled in <strong>Screen Options</strong> at the top right corner.</li>
				</ul>
				<figure class="img-polaroid"><img src="img/wordpress/post_format_standard.png" alt=""></figure>
				<p class="muted text-center">Standard post format</p>
			</article>

			<article id="post_format_aside">
				<h4>Aside</h4>
				<p>Aside is a simplified standard post format. In other words there is only a content field, so you don't need to enter title and other options.</p>
				<figure class="img-polaroid"><img src="img/wordpress/post_format_standard.png" alt=""></figure>
			</article>

			<article id="post_format_audio">
				<h4>Audio</h4>
				<figure class="img-polaroid"><img src="img/wordpress/post_format_audio.png" alt=""></figure>
				<p><strong>Audio format</strong> is used to publish audio content.</p>
				<figure class="img-polaroid"><img src="img/wordpress/post_format_audio_additional.png" alt=""></figure>
				<p>You can edit the following fields media gallery file settings:</p>
				<ul>
				<li><i class="fa fa-angle-right"></i> <strong>URL</strong> - audio file direct URL</li>
					<li><i class="fa fa-angle-right"></i> <strong>Title (Заголовок)</strong> - audio track name</li>
					<li><i class="fa fa-angle-right"></i> <strong>Artist (Исполнитель)</strong> - audio track performer</li>
					<li><i class="fa fa-angle-right"></i> <strong>Album (Альбом) </strong> - audio track album</li>
					<li><i class="fa fa-angle-right"></i> <strong>Description (Описание) </strong> - audio track description</li>
				</ul>
			</article>

			<article id="post_format_chat">
				<h4>Chat</h4>
				<figure class="img-polaroid"><img src="img/wordpress/post_format_chat.png" alt=""></figure>
				<p><strong> Chat </strong> format is a post with content in the form of successive messages</p>
				<ul>
					<li><i class="fa fa-angle-right"></i>Ann: Hello Mike!</li>
					<li><i class="fa fa-angle-right"></i>Mike: Hello Mike!</li>
					<li><i class="fa fa-angle-right"></i>Ann: How are you Mike?</li>
					<li><i class="fa fa-angle-right"></i>Mike: Not bad Ann.</li>
				</ul>
			</article>

			<article id="post_format_gallery">
				<h4>Gallery</h4>
				<p>Main feature of this post format is possibility to add unlimited number of images.</p>
				<p>Adding a gallery:</p>
				<ol>
					<li>Click<strong> Add Media</strong>.<br>
						<span class="img-polaroid"><img src="img/wordpress/add_media.png" alt=""></span></li>
					<li>Drag files or click <strong>Select Files</strong> to add images from local computer.
						<figure class="img-polaroid"><img src="img/wordpress/insert-media.png" alt=""></figure></li>
					<li>After images are uploaded, you can find them in Media Library. Click <strong>Uploaded to this post</strong> to view the images.
						<figure class="img-polaroid"><img src="img/wordpress/uploaded-images.png" alt=""></figure>
					</li>
					<li>After editing you can close the media library. Uploaded images will be added automatically.</li>
				</ol>
				<p>Gallery post form has the same fields as the standard post format has (e.g. title, content, category, etc).</p>
			</article>

			<article id="post_format_image">
				<h4>Image</h4>
				<figure class="img-polaroid"><img src="img/wordpress/post_format_image_editor.png" alt=""></figure>
				<p><strong>Image format </strong> is a format that includes one image uploaded through <strong>Featured Image</strong> <strong> option.</strong>.<br>
				<figure class="img-polaroid"><img src="img/wordpress/post_format_image.png" alt=""></figure>
					 Choosing this format you can turn off lightbox simply choosing proper option in the image settings <strong>Enable Lightbox</strong> .</p>
			</article>

			<article id="post_format_link">
				<h4>Link</h4>
				<figure class="img-polaroid"><img src="img/wordpress/post_format_link.png" alt=""></figure>
				<p><strong>Link</strong> post format can be created in the visual editor by adding a <strong>(URL)</strong> in standard format post.
				<strong>Post title</strong> will be used as the URL text.</p>
				<figure class="img-polaroid"><img src="img/wordpress/post_format_link_variant.png" alt=""></figure>
							<p class="muted text-center">Link with description</p>
				<p>Moreover you can add a description of the target page and link any part of the text.</p>
			</article>

			<article id="post_format_quote">
				<h4>Quote</h4>
				<figure class="img-polaroid"><img src="img/wordpress/post_format_quote.png" alt=""></figure>
				<p><strong> Quote </strong> post is used to publish statements.</p>

				<h4>Status</h4>
				<figure class="img-polaroid"><img src="img/wordpress/post_format_status.png" alt=""></figure>
				<p><strong> Status </strong> post is a message with content limited to 140 symbols. It is displayed with author avatar.</p>
			</article>

			<article id="post_format_video">
				<h4>Video</h4>
				<figure class="img-polaroid"><img src="img/wordpress/post_format_video.png" alt=""></figure>
				<p><strong> Video</strong> post is used to publish video content.</p>
				<p>There are two ways to add video into the post:</p>
				<ul>
					<li><i class="fa fa-angle-right"></i> <strong>Embedded Code</strong> - used to add video from external resource like Youtube, Vimeo, etc.</li>
					<li><i class="fa fa-angle-right"></i>Add video through media library. </li>
				</ul>
				<figure class="img-polaroid"><img src="img/wordpress/post_format_video_settings.png" alt=""></figure>
				<p>You can edit next fields in settings:</p>
				<ul>
					<li><i class="fa fa-angle-right"></i> URL #1</strong> Direct link to video in m4v format with possibility to delete</li>
					<li><i class="fa fa-angle-right"></i> Alternative source of various video formats (mp4, ogv, webm)</li>
					<li><i class="fa fa-angle-right"></i> Set an image for video poster</li>
					<li><i class="fa fa-angle-right"></i> Set video to autoplay</li>
					<li><i class="fa fa-angle-right"></i> Loop video</li>
				</ul>
			</article>
