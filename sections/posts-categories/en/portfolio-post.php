			<h4>Creating portfolio post</h4>
			<p>Cherry Portfolio is based on custom post type - Portfolio. To have portfolio post format available Cherry Portfolio plugin should be installed.</p>
			<p>To add new Portfolio post click "<b>Add New</b>" in portfolio section in WordPress admin panel.</p>
			<figure class="img-polaroid"><img src="img/wordpress/folio_panel.png" alt=""></figure>
			<p>As Portfolio posts are a variation of default WordPress post, creating new post should be a problem. You can <a href="http://codex.wordpress.org/Post_Types#Custom_Types"> learn more </a> about WordPress custom posts.</p>
			<ol>
				<li>Fill out all necessary fields (<strong>title</strong>, <strong>content</strong>).</li>
				<li>Upload image using <strong>Featured Image</strong> option (click<strong> Set featured image</strong>).</li>
				<li>Click<strong> Publish</strong>.</li>
			</ol>
			<figure class="img-polaroid"><img src="img/wordpress/folio.png" alt=""></figure>
			
			<h5>Choosing portfolio post format.</h5>
			<p>You can use next post types in portfolio post:</p>
			<ul>
				<li><i class="fa fa-angle-right"></i> <strong>Image</strong> - default gallery post type. Displays <em>post image</em> only.</li>
				<li><i class="fa fa-angle-right"></i> <strong>Slideshow</strong> - gallery project is displayed in slideshow. This gallery post type can contain unlimited number of images. </li>
				<li><i class="fa fa-angle-right"></i> <strong>Grid Gallery</strong> - post images are displayed in a grid gallery. This gallery post type can contain unlimited number of images.</li>
				<li><i class="fa fa-angle-right"></i> <strong>Video</strong> - you can add video from any  video hosting like YouTube, Vimeo, etc using this gallery post type.</li>
				<li><i class="fa fa-angle-right"></i> <strong>Audio</strong> - this gallery post type allows to add audio file. </li>
			</ul>
