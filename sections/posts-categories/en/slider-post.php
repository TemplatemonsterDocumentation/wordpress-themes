			<h4>Creating slider post</h4>			
			<p>Slides are	<a href="http://codex.wordpress.org/Post_Types#Custom_Types">custom type posts</a> and that is a variation of default WordPress posts (blog posts for example).</p>			
			<ol>
				<li>Click <strong>Slider</strong> tab.</li>
				<li>Click <strong>Add New</strong>.</li>
				<li>Enter <strong>title </strong> of the slide.</li>
				<li>Upload the image using <strong>Featured Image</strong> option(click <strong> Set featured image</strong>).</li>
				<li>You can use next fields with slide:
					<ul>
						<li><i class="fa fa-angle-right"></i> <strong>Caption</strong> - fields for slide caption.</li>
						<li><i class="fa fa-angle-right"></i> <strong>URL</strong> - field for slide URL (external link can be used).</li>
					</ul>
				</li>
				<li>Once slide settings are set up click<strong> Publish</strong>.</li>
			</ol>
