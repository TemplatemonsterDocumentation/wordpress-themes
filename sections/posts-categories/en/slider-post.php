<h3>Creating a Slider Post</h3>
<p>
    Slides are
    <a href="http://codex.wordpress.org/Post_Types#Custom_Types">custom type posts</a>
    and that is a variation of default WordPress posts (blog posts for example).
</p>

<ol class="index-list">
    <li>Click the <strong>Slider</strong> tab.</li>
    <li>Click <strong>Add New</strong>.</li>
    <li>Enter <strong>title </strong> of the slide.</li>
    <li>Upload an image using the <strong>Featured Image</strong> option (click <strong> Set featured image</strong>).
    </li>
    <li>You can use the following fields with a slide:
        <ul>
            <li><strong>Caption</strong> - field for the slide caption.</li>
            <li><strong>URL</strong> - field for slide URL (external link can be used).</li>
        </ul>
    </li>
    <li>Once the slide settings are set up, click Publish.</li>
</ol>

