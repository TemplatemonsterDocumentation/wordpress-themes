			<h4>Creating "Our Team" post</h4>
			<p>Cherry Team is based on custom post type - Team. To have team post format available Cherry Team plugin should be installed.</p>
			<p>As Team posts are a variation of default WordPress post, creating new post should be a problem. You can <a href="http://codex.wordpress.org/Post_Types#Custom_Types"> learn more </a> about WordPress custom posts.</p>	
			<ol>
				<li>Click <strong>Our Team</strong>.</li>
				<li>Click <strong>Add New</strong>.</li>
				<li>Fill out all necessary fields (<strong>title</strong>, <strong>content</strong>).</li>
				<li>Upload image using <strong>Featured Image</strong> option (click<strong> Set featured image</strong>).</li>
				<li>You can use next settings in "Our team" post:
					<ul>
						<li><i class="icon-angle-right"></i><strong>Position</strong> - person position</li>
						<li><i class="icon-angle-right"></i><strong>Info</strong> - additional information</li>
					</ul>
				</li>
				<li>Click<strong> Publish</strong></li>
			</ol>
