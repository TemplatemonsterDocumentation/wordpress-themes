<h3>Creating a Testimonial Post</h3>

<p>
    Cherry Testimonial is based on custom post type - Testimonial. To have the testimonial post format available, Cherry
    Testimonials plugin should be installed.
</p>

<p>
    As Testimonials posts are a variation of default WordPress post, creating new posts should not be a problem. You can
    <a href="http://codex.wordpress.org/Post_Types#Custom_Types"> learn more</a>
    about WordPress custom posts.
</p>

<ol class="index-list">
    <li>Click the <strong>Testimonials</strong> tab.</li>
    <li>Click <strong>Add New</strong>.</li>
    <li>Fill out all necessary fields (<strong>title</strong>, <strong>content</strong>).</li>
    <li>Upload image using the <strong>Featured Image</strong> option (click<strong> Set featured image</strong>).</li>
    <li>You can use the following settings in a testimonials post:
        <ul class="marked-list">
            <li><strong>Name</strong> - author's name</li>
            <li><strong>E-mail</strong> - author's email address</li>
            <li><strong>URL</strong> - author's URL.</li>
        </ul>
    </li>
    <li>Click<strong> Publish</strong>.</li>
</ol>

