<h3>Creating a Blog Post</h3>

<p>To add a blog post you need to do the following:</p>

<ol class="index-list">
    <li>Open the <strong>Posts</strong> tab.</li>
    <li>Click <strong>Add New</strong>.</li>
    <li>Add content to the post.</li>
</ol>

