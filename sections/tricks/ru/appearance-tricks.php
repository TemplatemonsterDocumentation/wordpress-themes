
	<h3>Приемы с оформлением</h3>

	<h4>Заголовок с подписью</h4>
	<p>Для отображения заголовка и текстовой подписи страницы следующего вида, вам необходимо проделать следующие шаги:</p>

	<figure class="img-polaroid">
		<img src="img/cherryframework/breadcrumbs_tricks_example.png" alt="" style="opacity: 1;">
	</figure>

	<ol class="index-list">
		<li>
			<p>Вам необходимо включить опцию <strong>Breadcrumbs (Навигационная цепочка)</strong> настройка в <strong>Cherry Options:</strong></p>
			<figure class="img-polaroid">
				<img src="img/cherryframework/breadcrumbs_tricks_option.png" alt="" style="opacity: 1;">
			</figure>
		</li>
		<li>
			<p>
				В блоке <strong>Additional page options (Дополнительные натройки страницы)</strong> каждой страницы доступны несколько типов отображения на выбор:
			</p>
			<ul class="marked-list">
				<li>
					Наследовать
				</li>
				<li>
					Не отображать
				</li>
				<li>
					Отображать оба - цепочку и название
				</li>
				<li>
					Отображать только название
				</li>
				<li>
					Отображать только цепочку
				</li>
			</ul>
			<figure class="img-polaroid">
				<img src="img/cherryframework/breadcrumbs_tricks_page_options.png" alt="" style="opacity: 1;">
			</figure>
			<p>
				Вам необходимо выбрать <strong>Show both - breadcrumbs and title (Отображать оба - цепочку и название).</strong>
			</p>
		</li>
		<li>
			<p>Фоновое изображение задается в блоке настроек страницы - <strong>Featured image.</strong></p>
			<figure class="img-polaroid">
				<img src="img/cherryframework/breadcrumbs_tricks_featured.png" alt="" style="opacity: 1;">
			</figure>
		</li>
		<li>
			<p>
				Для того, чтобы вывести “подпись под заголовком”, необходимо в настройках страницы <strong>Screen Options</strong>, в правом верхнем углу отметить опцию <strong>Excerpt</strong>.
			</p>
			<figure class="img-polaroid">
				<img src="img/cherryframework/screen_options_tricks.png" alt="" style="opacity: 1;">
			</figure>
		</li>
	</ol>
