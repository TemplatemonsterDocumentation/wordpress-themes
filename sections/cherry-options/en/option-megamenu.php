<h3>Megamenu</h3>

<p>The section contains settings for Megamenu plugin.</p>

<div class="alert alert-info">
    Attention, Megamenu options will be available only in case you have - "<a href="index.php?project=monstroid&lang=en&section=cherry-plugins#megamenu">Cherry Megamenu</a> plugin installed.
</div>

<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Main theme menu location</dt>
            <dd>
                Define the menu location for your main navigation.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Mega menu enabled</dt>
            <dd>
                Display / Hide megamenu.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Animation effect</dt>
            <dd>
                Define animation effects for the dropdown menu.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Event</dt>
            <dd>
                Define an activation event for megamenu.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Navigation parent container CSS selector</dt>
            <dd>
                Define the CSS selector for mega menu parent container (if needed). By default - ".container"
            </dd>
        </dl>
    </li>

</ul>

