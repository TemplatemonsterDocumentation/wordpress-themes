<h3>Landing Page</h3>


<p>In case you need to create a landing page for your website, please do the following:</p>

<ol class="index-list">
    <li>Create new page</li>
    <li>
        <p>Adding landing page content use anchor parameter in row shortcode to create navigation anchors</p>
        <figure class="img-polaroid">
            <img src="img/cherryframework/landing_navigation_anchor.png" alt="" style="opacity: 1;">
        </figure>
    </li>
    <li>When you are done adding page content, go to <b>Cherry Options</b></li>
    <li>
        <p>In <b>General</b> section find <b>Lading Page</b> option. Select your recently created page to serve as
           landing page</p>

        <figure class="img-polaroid">
            <img src="img/cherryframework/landing_navigation.png" alt="" style="opacity: 1;">
        </figure>
    </li>
    <li>Save Cherry Options to apply changes</li>
</ol>


