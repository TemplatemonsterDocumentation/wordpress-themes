	<p>In case you need to create a landing page for your website, please do the following:</p>

	<ol>
		<li>Create new page</li>
		<li>Adding landing page content use anchor parameter in row shortcode to create navigation anchors</li>
		<li>When you are done adding page content, go to <b>Cherry Options</b></li>
		<li>In <b>General</b> section find <b>Lading Page</b> option. Select your recently created page to serve as landing page</li>
		<li>Save Cherry Options to apply changes</li>
	</ol>
