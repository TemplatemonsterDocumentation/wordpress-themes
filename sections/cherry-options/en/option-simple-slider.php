<h3>Simple slider</h3>

<p>The section contains options for displaying and settings slider on site</p>

<div class="alert alert-info">
    Attention! Simple slider options will be available only in case you have - "<a href="index.php?project=monstroid&lang=ru&section=cherry-plugins#simple-slider">Cherry Simple Slider</a>"
</div>


<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Slider name</dt>
            <dd>
                Define slider name
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Number of slides</dt>
            <dd>
                Define total number of slides
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Slides order</dt>
            <dd>
                Define order type of slide, it takes two parameters:
                <ul class="marked-list">
                    <li>
                        Descending order from highest to lowest values
                    </li>
                    <li>
                        Ascending order from lowest to highest values
                    </li>
                </ul>
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Slider width</dt>
            <dd>
                Define the width of the slider
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Slider height</dt>
            <dd>
                Define the height of the slider
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Slider 992px breakpoint height</dt>
            <dd>
                Define specific breakpoints which allow changing the look and behavior of the slider when the page resizes to 992px.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Slider 768px breakpoint height</dt>
            <dd>
                Define specific breakpoints which allow changing the look and behavior of the slider when the page resizes to 768px.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Slider visible size</dt>
            <dd>
                Define the size of the visible area, allowing  more slides to become visible near the selected slide.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Slider force Size</dt>
            <dd>
                Indicates if the size of the slider will be forced to full width or full window.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Slider orientation</dt>
            <dd>
                Indicates whether the slides will be arranged horizontally or vertically.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Slide distance</dt>
            <dd>
                Defines the distance between the slides.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Slide animation duration</dt>
            <dd>
                Defines the duration of the slide animation.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Slide fading</dt>
            <dd>
                Enable / Disable - indicates if fade effect will be used.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Slider navigation</dt>
            <dd>
                Enable / Disable slider navigation.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Slider fade navigation</dt>
            <dd>
                Indicates whether the arrows will fade in only on hover.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Slider pagination</dt>
            <dd>
                Enable / Disable slider pagination.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Slider autoplay</dt>
            <dd>
                Enable / Disable slider autoplay.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Slider full Screen</dt>
            <dd>
                Indicates whether the full-screen button is enabled.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Slider shuffle</dt>
            <dd>
                Indicates if the slides will be shuffled.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Slider loop</dt>
            <dd>
                Enable / Disable slider loop.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Thumbnails</dt>
            <dd>
                Enable / Disable slider thumbnails.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Thumbnail arrows</dt>
            <dd>
                Enable / Disable slider thumbnails arrows.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Thumbnails Position</dt>
            <dd>
                Define the position of the thumbnail scroller.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Thumbnail width</dt>
            <dd>
                Define the thumbnail width
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Thumbnail height</dt>
            <dd>
                Define the thumbnail height
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Reach video action</dt>
            <dd>
                Define the action that the video will perform when its slide container is selected.
            </dd>
        </dl>
    </li>
</ul>


