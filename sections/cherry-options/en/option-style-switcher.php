<h3>Style switcher</h3>

<p>
    The section contains options for customizing the appearance of your site.
</p>


<div class="alert alert-info">
    Attention! Style Switcher options will be available only in case you have installed the "<a href="index.php?project=monstroid&lang=ru&section=cherry-plugins#style-switcher">Cherry Style Switcher</a>" plugin.
</div>


<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Style Switcher</dt>
            <dd>
                Enable / Disable displaying of setting sidebar, on a public version of site
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Visible To:</dt>
            <dd>
                Define user group access, for using setting sidebar on a public version of site
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Custom style CSS</dt>
            <dd>
                Visual editor for custom css styles
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Demo mode</dt>
            <dd>
                Enable / Disable demo mode for unregistered users
            </dd>
        </dl>
    </li>
</ul>

