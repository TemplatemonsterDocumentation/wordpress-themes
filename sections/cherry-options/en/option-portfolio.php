    <h3>Portfolio</h3>

    <p>The section contains settings for WordPress blog pages (listing and single post types).</p>


    <div class="alert alert-info">
        Attention, portfolio options will be available only in case you have - "<a href="index.php?project=monstroid&lang=en&section=cherry-plugins#portfolio">Cherry Portfolio</a>" plugin installed.
    </div>


