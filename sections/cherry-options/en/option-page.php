<h3>Page</h3>

<p>General page options section.</p>


<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Background</dt>
            <dd>Page background settings. You can select background color, upload page background image, set its
                background
                position, attachment and repeat.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Grid type</dt>
            <dd>Select the layout pattern for the main website container. Wide layout will fit window width. Boxed
                layout
                will
                have fixed width and left/right indents.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Boxed width</dt>
            <dd>Header section width if boxed layout is active. Should not be more than the Grid -> Container width
                value.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Featured Images</dt>
            <dd>Enable/disable featured images for pages.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Page comments</dt>
            <dd>Enable/disable comments by default for pages. For pages that have already been published, you need to
                enable
                comments individually in page settings.
            </dd>
        </dl>
    </li>
</ul>


