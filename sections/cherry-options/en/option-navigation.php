	<h4>Pagination</h4>
	<figure class="img-polaroid">
		<img src="img/cherryframework/cherry-options_pagination.jpg" alt="" style="opacity: 1;"><i class="icon-search"></i><span></span>
	</figure>
	<dl>
		<dt>Pagination position</dt>
		<dd>
			<p>Select where you want to display pagination.</p>
		</dd>

		<dt>Prev/next buttons</dt>
		<dd>
			<p>Show/hide previous and next buttons in pagination.</p>
		</dd>

		<dt>Pagination label</dt>
		<dd>
			<p>Pagination label is displayed before pagination buttons. Text or HTML can be used.</p>
		</dd>

		<dt>Prev button label</dt>
		<dd>
			<p>Previous button label text. Text or HTML can be used.</p>
		</dd>

		<dt>Next button label</dt>
		<dd>
			<p>Next button label text. Text or HTML can be used.</p>
		</dd>

		<dt>Show all the pages</dt>
		<dd>
			<p>If set to On, it will show all of the pages instead of a short list of pages near the current page.</p>
		</dd>

		<dt>End size</dt>
		<dd>
			<p>How many numbers to display on either the start or the end list edges.</p>
		</dd>

		<dt>Mid size</dt>
		<dd>
			<p>How many numbers to display to either side of the current page, but not including the current page.</p>
		</dd>
	</dl>

