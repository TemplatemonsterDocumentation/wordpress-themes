<h3>Create products</h3>

<ul class="index-list">
    <li>Click on the Products sidebar menu and click on “Add Product” button. Enter a name for
        your product at the top of the page.
    </li>
    <li>Enter the product description text into the post content field, this will be all the
        product information.
    </li>
    <li>The “Product Data” box is where you enter all the product details (price, SKU,
        shipping, and more).
    </li>
    <li>The “Product Short Description” box is for adding a short intro text that will show up
        next to your main images.
    </li>
    <li>Set your main “Featured Image” in the right sidebar Featured Image box. This has to be
        done for every product.
    </li>
    <li>To use a gallery of images, insert more images in the “Product Gallery” box.</li>
    <li>Enter your categories in the “Product Categories” box and enter your tags in the
        “Product Tags” box.
    </li>
    <li>When all your data is entered, click “Publish” and the item will now show on your main
        shop page.
    </li>
</ul>
