	<h3>Create products</h3>	
	<ul>
		<li><strong>Step 1</strong> – Click on the Products sidebar menu and click on “Add Product” button. Enter a name for your product at the top of the page.</li>
		<li><strong>Step 2</strong> – Enter the product description text into the post content field, this will be all the product information.</li>
		<li><strong>Step 3</strong> – The “Product Data” box is where you enter all the product details (price, SKU, shipping, and more).</li>
		<li><strong>Step 4</strong> – The “Product Short Description” box is for adding a short intro text that will show up next to your main images.</li>
		<li><strong>Step 5</strong> – Set your main “Featured Image” in the right sidebar Featured Image box. This has to be done for every product.</li>
		<li><strong>Step 6</strong> – To use a gallery of images, insert more images in the “Product Gallery” box.</li>
		<li><strong>Step 7</strong> – Enter your categories in the “Product Categories” box and enter your tags in the “Product Tags” box.</li>
		<li><strong>Step 8</strong> – When all your data is entered, click “Publish” and the item will now show on your main shop page.</li>
	</ul>	
